								<p>Geraldine Hickey has been described as a ‘deceptively cheery dame who'll quietly take out your innards.’ She is revered in the industry as a comedian’s comedian. This was proven in 2011 and 2012 when she was shortlisted for the ‘Piece of Wood’ award at the Melbourne International Comedy Festival. A veteran of 8 seasons of The Melbourne International Comedy Festival and a regular on the club and pub circuit around the country her experience has grown her into the thinking crowd’s crumpet.</p>
								<p>Last year Geraldine confirmed what it seems many of her fans already knew – she’s come out of the closet. Her latest show ‘Turns Out I Do Like Sun Dried Tomatoes’ is a look back at the signs she may have missed to indicate she is totally gay. Never one to shy away from some brutal honesty this latest show will prove to be some of her finest work to date.</p>
