							    <p>
							        In 2010 Anne Edmonds made the national finals of the Raw Comedy Awards – 
							        <em>(A national talent quest to unearth Australia’s hottest new comedy talent) </em>
							        and performed her debut Melbourne International Comedy Festival show, 
							        <em>“Sing Us a Song You’re the Piano Anne” to sell-out festival crowds.</em>
							    </p>
							    <p>
							        Joining forces once again with musician Amy Bennett, Anne then wrote her second show, 
							        <em>“Ever Since the Dawn of Anne”</em>, for the 2010 Melbourne Fringe Festival. 
							        The show was nominated for the Best Comedy Award and was ‘Highly Commended’ by the Melbourne Fringe Festival judges. 
							        At the 2011 Adelaide Fringe Festival it was also nominated as Best Emerging Comedy. 
							        Anne's run of the show at the 2011 Melbourne International Comedy Festival culminated with her receiving 
							        four star reviews from well respected newspaper’s including <em>The Age</em>. 
							    </p>
							    <p>
							        The 2011 Melbourne Fringe Festival saw the debut of Anne's third show; <em>“My Banjo's Name is Steven”</em>, 
                                    this time receiving her four and a half star reviews from <em>The Age</em>. Again, her fringe festival season quickly became a sell out hit. 
                                    Anne now has a solid fan base who loyally follow her unique style of comedy.
							    </p>
							    <p>
							        Anne was a cast member of Channel 31’s Studio A sketch comedy show and has been featured on Triple M, 
							        ABC Radio Melbourne and Triple R. In 2010 she was chosen for the Melbourne Fringe’s Outside Eye professional development program 
							        and mentored by legendary Australian comedian John Clarke. 
							        She was named a ‘Star on the Rise’ in The Age newspaper’s Melbourne Magazine Awards for the second year in a row (2011) 
							        and described as <em>“a relative newcomer who has carved out an utterly distinctive corner of off-kilter comedy”</em>.
							    </p>
                                <p>Anne will make her international debut at the Edinburgh Fringe Festival in August 2012.</p>
								
